/*
Operadores de comparação
> maior que
>= maior que ou igual a
< menor que
<= menor que ou igual a
== igualdade (valor) - não se utiliza, pois é feito uma correção de tipos ou seja um numero 10 é igual a uma string 10
=== igualdade estrita (valor e tipo)
!= diferente (valor) - não se utiliza
!== diferente estrito (valor e tipo)
*/

/*
Operadores lógicos

&& - and - todas as expressões precisam ser verdadeiras para retornar verdadeiro
|| - or - só uma expressão precisa ser verdadeira
! - not - nega a expressão (inverte o valor)
*/